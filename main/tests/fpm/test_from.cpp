#include "common.hpp"

using P = fpm::fixed_16_16;

TEST(from, fixed_point)
{
    //0.5
    EXPECT_EQ(P::from_raw_value(0x008100),
              P::from_fixed_point<8>(0b0000'0000'1000'0001)
    );
    EXPECT_EQ(P::from_raw_value(0x008000),
              P::from_fixed_point<8>(0b0000'0000'1000'0000)
    );
    EXPECT_EQ(P::from_raw_value(0x007f00),
              P::from_fixed_point<8>(0b0000'0000'0111'1111)
    );
    //-0.5
    EXPECT_EQ(P::from_raw_value(0xffff'8100),
              P::from_fixed_point<8>(0xff'ff'ff'ff'ff'ff'ff'81)
    );
    EXPECT_EQ(P::from_raw_value(0xffff'8000),
              P::from_fixed_point<8>(0xff'ff'ff'ff'ff'ff'ff'80)
    );
    EXPECT_EQ(P::from_raw_value(0xffff'7f00),
              P::from_fixed_point<8>(0xff'ff'ff'ff'ff'ff'ff'7f)
    );

    //0.5
    EXPECT_EQ(P::from_raw_value(0b1000'0000'0000'0001),
              P::from_fixed_point<18>(0x2'00'00ll+4ll)
    );
    EXPECT_EQ(P::from_raw_value(0b1000'0000'0000'0001),
              P::from_fixed_point<18>(0x2'00'00ll+3ll)
    );
    EXPECT_EQ(P::from_raw_value(0b1000'0000'0000'0001),
              P::from_fixed_point<18>(0x2'00'00ll+2ll)
    );
    EXPECT_EQ(P::from_raw_value(0b1000'0000'0000'0000),
              P::from_fixed_point<18>(0x2'00'00ll+1ll)
    );
    EXPECT_EQ(P::from_raw_value(0b1000'0000'0000'0000),
              P::from_fixed_point<18>(0x2'00'00ll)
    );
    EXPECT_EQ(P::from_raw_value(0b1000'0000'0000'0000),
              P::from_fixed_point<18>(0x2'00'00ll-1ll)
    );
    EXPECT_EQ(P::from_raw_value(0b1000'0000'0000'0000),
              P::from_fixed_point<18>(0x2'00'00ll-2ll)
    );
    EXPECT_EQ(P::from_raw_value(0b1000'0000'0000'0000-1),
              P::from_fixed_point<18>(0x2'00'00ll-3ll)
    );
    EXPECT_EQ(P::from_raw_value(0b1000'0000'0000'0000-1),
              P::from_fixed_point<18>(0x2'00'00ll-4ll)
    );
    //-0.5
    EXPECT_EQ(P::from_raw_value(0xffff'8000+1),
              P::from_fixed_point<18>(0xffff'ffff'fffe'0000ll+4ll)
    );
    EXPECT_EQ(P::from_raw_value(0xffff'8000+1),
              P::from_fixed_point<18>(0xffff'ffff'fffe'0000ll+3ll)
    );
    EXPECT_EQ(P::from_raw_value(0xffff'8000),
              P::from_fixed_point<18>(0xffff'ffff'fffe'0000ll+2ll)
    );
    EXPECT_EQ(P::from_raw_value(0xffff'8000),
              P::from_fixed_point<18>(0xffff'ffff'fffe'0000ll+1ll)
    );
    EXPECT_EQ(P::from_raw_value(0xffff'8000),
              P::from_fixed_point<18>(0xffff'ffff'fffe'0000ll)
    );
    EXPECT_EQ(P::from_raw_value(0xffff'8000),
              P::from_fixed_point<18>(0xffff'ffff'fffe'0000ll-1ll)
    );
    EXPECT_EQ(P::from_raw_value(0xffff'8000-1),
              P::from_fixed_point<18>(0xffff'ffff'fffe'0000ll-2ll)
    );
    EXPECT_EQ(P::from_raw_value(0xffff'8000-1),
              P::from_fixed_point<18>(0xffff'ffff'fffe'0000ll-3ll)
    );
    EXPECT_EQ(P::from_raw_value(0xffff'8000-1),
              P::from_fixed_point<18>(0xffff'ffff'fffe'0000ll-4ll)
    );
}

TEST(from, custom_fraction)
{
    //0.5
    EXPECT_EQ(P::from_custom_fraction<(1ll<<8)>(
                  0ll,(1ll<<7)+1ll),
              P::from_raw_value(0b1000'0001'0000'0000)
    );
    EXPECT_EQ(P::from_custom_fraction<(1ll<<8)>(
                  0ll,(1ll<<7)),
              P::from_raw_value(0b1000'0000'0000'0000)
    );
    EXPECT_EQ(P::from_custom_fraction<(1ll<<8)>(
                  0ll,(1ll<<7)-1ll),
              P::from_raw_value(0b0111'1111'0000'0000)
    );
    //-0.5
    EXPECT_EQ(P::from_custom_fraction<(1ll<<8)>(
                  0ll,-(1ll<<7)+1ll),
              P::from_raw_value(0xffff'8100)
    );
    EXPECT_EQ(P::from_custom_fraction<(1ll<<8)>(
                  0ll,-(1ll<<7)),
              P::from_raw_value(0xffff'8000)
    );
    EXPECT_EQ(P::from_custom_fraction<(1ll<<8)>(
                  0ll,-(1ll<<7)-1ll),
              P::from_raw_value(0xffff'7f00)
    );

    //0.5
    EXPECT_EQ(P::from_custom_fraction<(1ll<<18)>(
                  0ll,(1ll<<17)+4ll),
              P::from_raw_value(0x80'00+1)
    );
    EXPECT_EQ(P::from_custom_fraction<(1ll<<18)>(
                  0ll,(1ll<<17)+3ll),
              P::from_raw_value(0x80'00+1)
    );
    EXPECT_EQ(P::from_custom_fraction<(1ll<<18)>(
                  0ll,(1ll<<17)+2ll),
              P::from_raw_value(0x80'00+1)
    );
    EXPECT_EQ(P::from_custom_fraction<(1ll<<18)>(
                  0ll,(1ll<<17)+1ll),
              P::from_raw_value(0x80'00)
    );
    EXPECT_EQ(P::from_custom_fraction<(1ll<<18)>(
                  0ll,(1ll<<17)),
              P::from_raw_value(0x80'00)
    );
    EXPECT_EQ(P::from_custom_fraction<(1ll<<18)>(
                  0ll,(1ll<<17)-1ll),
              P::from_raw_value(0x80'00)
    );
    EXPECT_EQ(P::from_custom_fraction<(1ll<<18)>(
                  0ll,(1ll<<17)-2ll),
              P::from_raw_value(0x80'00)
    );
    EXPECT_EQ(P::from_custom_fraction<(1ll<<18)>(
                  0ll,(1ll<<17)-3ll),
              P::from_raw_value(0x80'00-1)
    );
    EXPECT_EQ(P::from_custom_fraction<(1ll<<18)>(
                  0ll,(1ll<<17)-4ll),
              P::from_raw_value(0x80'00-1)
    );
    //-0.5
    EXPECT_EQ(P::from_custom_fraction<(1ll<<18)>(
                  0ll,-(1ll<<17)+4ll),
              P::from_raw_value(0xff'ff'80'00+1)
    );
    EXPECT_EQ(P::from_custom_fraction<(1ll<<18)>(
                  0ll,-(1ll<<17)+3ll),
              P::from_raw_value(0xff'ff'80'00+1)
    );
    EXPECT_EQ(P::from_custom_fraction<(1ll<<18)>(
                  0ll,-(1ll<<17)+2ll),
              P::from_raw_value(0xff'ff'80'00)
    );
    EXPECT_EQ(P::from_custom_fraction<(1ll<<18)>(
                  0ll,-(1ll<<17)+1ll),
              P::from_raw_value(0xff'ff'80'00)
    );
    EXPECT_EQ(P::from_custom_fraction<(1ll<<18)>(
                  0ll,-(1ll<<17)),
              P::from_raw_value(0xff'ff'80'00)
    );
    EXPECT_EQ(P::from_custom_fraction<(1ll<<18)>(
                  0ll,-(1ll<<17)-1ll),
              P::from_raw_value(0xff'ff'80'00)
    );
    EXPECT_EQ(P::from_custom_fraction<(1ll<<18)>(
                  0ll,-(1ll<<17)-2ll),
              P::from_raw_value(0xff'ff'80'00-1)
    );
    EXPECT_EQ(P::from_custom_fraction<(1ll<<18)>(
                  0ll,-(1ll<<17)-3ll),
              P::from_raw_value(0xff'ff'80'00-1)
    );
    EXPECT_EQ(P::from_custom_fraction<(1ll<<18)>(
                  0ll,-(1ll<<17)-4ll),
              P::from_raw_value(0xff'ff'80'00-1)
    );
}
